    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    try {
            if (txtPatente.getText().isEmpty() || txtMarca.getText().isEmpty() || 
                txtModelo.getText().isEmpty() || txtAnio.getText().isEmpty() || 
                txtCapacidadCarga.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Complete todos los campos obligatorios");
                return;
            }
            
            if (comboSucursal.getSelectedIndex() <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una sucursal válida");
                return;
            }
            
            String patente = txtPatente.getText();
            String nombreMarca = txtMarca.getText();
            String modelo = txtModelo.getText();
            int anio = Integer.parseInt(txtAnio.getText());
            double capacidad = Double.parseDouble(txtCapacidadCarga.getText());
            
            Marca marca = new Marca(nombreMarca, "No especificado");
            
            String sucursalSeleccionada = (String) comboSucursal.getSelectedItem();
            String[] partes = sucursalSeleccionada.split(" - ");
            String codigo = partes[0].replace("Sucursal ", "SUC0");
            String direccion = partes.length > 1 ? partes[1] : "Dirección no especificada";
            
            Responsable responsableTemp = new Responsable("Admin", "00000000", "0000000000");
            Sucursal sucursal = new Sucursal(codigo, direccion, "Tucumán", responsableTemp);
            
            String tipoSeleccionado = (String) comboTipo.getSelectedItem();
            Vehiculo nuevoVehiculo = null;
            
            if ("Eléctrico".equals(tipoSeleccionado)) {
                if (txtDatoEspecifico.getText().isEmpty()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el consumo en kWh/100km");
                    return;
                }
                double kwhBase = Double.parseDouble(txtDatoEspecifico.getText());
                nuevoVehiculo = new VehiculoElectrico(patente, marca, modelo, anio, capacidad, sucursal, kwhBase);
                
            } else if ("Combustible".equals(tipoSeleccionado)) {
                if (txtDatoEspecifico.getText().isEmpty() || txtLitrosExtra.getText().isEmpty()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Complete rendimiento y litros extra");
                    return;
                }
                double kmPorLitro = Double.parseDouble(txtDatoEspecifico.getText());
                double litrosExtra = Double.parseDouble(txtLitrosExtra.getText());
                nuevoVehiculo = new VehiculoCombustible(patente, marca, modelo, anio, capacidad, sucursal, kmPorLitro, litrosExtra);
            }
            
            if (nuevoVehiculo != null) {
                Persistencia.agregarVehiculo(nuevoVehiculo);
                javax.swing.JOptionPane.showMessageDialog(this, "Vehículo agregado con éxito!\n" + 
                                              "Patente: " + patente + "\n" +
                                              "Marca: " + nombreMarca + "\n" +
                                              "Sucursal: " + sucursal.getCodigo());
                dispose();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error: \n" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

