    private void configurarEventoTipo() {
        comboTipo.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                String seleccion = (String) comboTipo.getSelectedItem();
                
                if ("Eléctrico".equals(seleccion)) {
                    labelDatoEspecifico.setText("Consumo (kWh/100km):");
                    txtDatoEspecifico.setEnabled(true);
                    txtLitrosExtra.setEnabled(false);
                    txtLitrosExtra.setText("");
                } else if ("Combustible".equals(seleccion)) {
                    labelDatoEspecifico.setText("Rendimiento (Km/Litro):");
                    txtDatoEspecifico.setEnabled(true);
                    txtLitrosExtra.setEnabled(true);
                } else {
                    labelDatoEspecifico.setText("Dato específico:");
                    txtDatoEspecifico.setEnabled(false);
                    txtLitrosExtra.setEnabled(false);
                    txtDatoEspecifico.setText("");
                    txtLitrosExtra.setText("");
                }
            }
        });
    }

